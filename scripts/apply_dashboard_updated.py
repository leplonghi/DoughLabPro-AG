def load_json(path):
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def save_json(path, data):
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)

def update_keys(data, translations, prefix=""):
    modified = False
    for k, v in data.items():
        curr_key = f"{prefix}.{k}" if prefix else k
        
        if isinstance(v, str):
            if curr_key in translations:
                if data[k] != translations[curr_key]:
                    data[k] = translations[curr_key]
                    modified = True
        elif isinstance(v, dict):
            if update_keys(v, translations, curr_key):
                modified = True
    return modified

